public class Edge {
	public final String id;
	public final String node1;
	public final String node2;
	public final double weight;

	public Edge(String id, String node1, String node2, double weight) {
		this.id = id;
		this.node1 = node1;
		this.node2 = node2;
		this.weight = weight;
	}

}